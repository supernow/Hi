	public int recordFile(int bits, int rate, int nchannels, String filepath,
			int timeout, int denoiseLevel) {
		new Thread(new MHRunnable(new RecordParam(bits, rate, nchannels,
				filepath, timeout, denoiseLevel)) {
			public void run(Object o) {
				RecordParam param = (RecordParam) o;
				// initDenoise(param.denoiseLevel, param.rate);

				System.out.println("start record[" + param.filepath + "].");

				int frameLen = param.rate * 20 / 1000 * param.bits / 8;// 320;
																		// //
																		// 20ms
				int framenum = param.timeout * 1000 / 20; // 20ms

				AudioRecord audioRecord = null;
				DataOutputStream dos = null;

				try {
					dos = new DataOutputStream(new BufferedOutputStream(
							new FileOutputStream(new File(param.filepath))));

					int bufferSize = AudioRecord.getMinBufferSize(param.rate,
							AudioFormat.CHANNEL_CONFIGURATION_MONO,
							(param.bits == 16) ? AudioFormat.ENCODING_PCM_16BIT
									: AudioFormat.ENCODING_PCM_8BIT);
					System.out.println("record buffer size:" + bufferSize);

					audioRecord = new AudioRecord(
							MediaRecorder.AudioSource.MIC, param.rate,
							AudioFormat.CHANNEL_CONFIGURATION_MONO,
							(param.bits == 16) ? AudioFormat.ENCODING_PCM_16BIT
									: AudioFormat.ENCODING_PCM_8BIT, bufferSize);

					if (audioRecord == null || dos == null)
						return;

					System.out
							.println("-----------------begin record-----------------.");
					if (mscb != null)
						mscb.notifyEvt(IMediaHelperCb.recordfile_beg, null);
					byte[] buffer = new byte[frameLen];
					drt.begRecordTick = System.currentTimeMillis();
					audioRecord.startRecording();
					// System.out.println("-----recording-----");
					drt.recordBufLen = ((System.currentTimeMillis() - drt.begRecordTick) / 20 - 1) * 20;

					isRecording = true;
					while (isRecording && (framenum-- >= 0)) {
						audioRecord.read(buffer, 0, frameLen);
						// System.out.println(".....read.....");
						dos.write(buffer);
					}

					if (isRecording) {
						System.out.println("record timeout.");
					}

					if (mscb != null)
						mscb.notifyEvt(IMediaHelperCb.recordfile_end, null);
				} catch (Throwable e) {
					e.printStackTrace();
				} finally {
					if (audioRecord != null) {
						audioRecord.stop();
						audioRecord.release();
					}
					try {
						if (dos != null)
							dos.close();
					} catch (IOException e) {
						e.printStackTrace();
					}

					// uninitDenoise();
				}
			}
		}).start();
		return 0;
	}

